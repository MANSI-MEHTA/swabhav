package com.tectlabs.cmptest;

public class Cmp {
	private int year;
	private String name;
	public Cmp(int year,String name) {
		super();
		this.year = year;
		this.name=name;
	}

	public int getYear() {
		return year;
	}
	public String getName() {
		return name;
	}

	public void setYear(int year) {
		this.year = year;
	}
	public void setname(String name) {
		this.name = name;
	}

	

}
